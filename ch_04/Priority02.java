
public class Priority02
{
   public static void main(String[] args)
   {

      System.out.print("72 / 10 + 72 % 10 = ");
      System.out.println(72 / 10 + 72 % 10);

      System.out.print("22 - - 3 * - 4 + - - 1 = ");
      System.out.println(22 - - 3 * - 4 + - - 1);

      System.out.print("(22 - - 3) * - (4 + - - 1) = ");
      System.out.println((22 - - 3) * - (4 + - - 1));
   }
}
