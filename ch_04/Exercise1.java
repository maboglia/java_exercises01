public class Exercise1
{

   public static void main(String[] args) 
   {

      int a = 15;
      int b = 24;

      System.out.println(b - a + 7);
      System.out.println(b - a - 4);

      System.out.println(b % a / 2);
      System.out.println(b % (a / 2));

      System.out.println(b * a / 2);
      System.out.println(b * (a / 2));

      System.out.println(b / 2 * a);
      System.out.println(b / (2 * a));
   }
}

