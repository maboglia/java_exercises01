
public class Increment01
{
   public static void main(String[] args)
   {
      int a;

      a = 4;
      System.out.println(++a);
      System.out.println(a);
      System.out.println();

      a = 4;
      System.out.println(a++);
      System.out.println(a);
      System.out.println();

      a = 4;
      System.out.println(--a);
      System.out.println(a);
      System.out.println();

      a = 4;
      System.out.println(a--);
      System.out.println(a);
      System.out.println();
   }
}
