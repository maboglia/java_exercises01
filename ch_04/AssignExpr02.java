
public class AssignExpr02
{
   public static void main(String[] args)
   {
      int a;

      a = 5;
      System.out.println(a = 2);
      a = 5;
      System.out.println(a += 2);
      a = 5;
      System.out.println(a -= 2);
      a = 5;
      System.out.println(a *= 2);
      a = 5;
      System.out.println(a /= 2);
      a = 5;
      System.out.println(a %= 2);
   }
}
