public class Shorthand1
{
   public static void main(String[] args)
   {
      int x;

      x = 7;
      x += 4;
      System.out.println(x);
  
      x = 7;
      x -= 4;
      System.out.println(x);

      x = 7;
      x *= 4;
      System.out.println(x);

      x = 7;
      x /= 4;
      System.out.println(x);

      x = 7;
      x %= 4;
      System.out.println(x);
   }
}
