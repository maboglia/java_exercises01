// Operatore modulo e approssimazione nei calcoli
class Modulo{
	public static void main(String args[]){
		int i1 = 5, i2 = 6, i3;
		double d1 = 1.2, d2 = 6.1,d3;
		i3 = i2 % i1;
		System.out.println(i2 + "%" + i1 + " vale " + i3);
		d3 = d2 % d1;
		System.out.println(d2 + "%" + d1 + " vale " + d3);
	}
}